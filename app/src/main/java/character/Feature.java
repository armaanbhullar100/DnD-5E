package character;

public class Feature {
    private String name;
    private String description;
    private String source;
    private int level;

    public Feature() {

    }

    public String getName() {
        return this.name;
    }

    public String getDescription() {
        return this.description;
    }

    public String getSource() {
        return this.source;
    }

    public int getLevel() {
        return this.level;
    }

}
