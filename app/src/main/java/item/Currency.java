package item;

public class Currency {
    int copperPieces;
    int silverPieces;
    int electrumPieces;
    int goldPieces;
    int platinumPieces;

    public Currency(int copperPieces, int silverPieces, int electrumPieces, int goldPieces, int platinumPieces) {
        this.copperPieces = copperPieces;
        this.silverPieces = silverPieces;
        this.electrumPieces = electrumPieces;
        this.goldPieces = goldPieces;
        this.platinumPieces = platinumPieces;
    }
}
